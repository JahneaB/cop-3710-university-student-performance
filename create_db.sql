SELECT s.First_Name, s.Last_Name, s.School_Name, e.Course_Code, e.G3
FROM STUDENT s
JOIN ENROLLMENT e ON s.Student_ID = e.Student_ID
ORDER BY e.G3 DESC;

SELECT b.Weekend_Alcohol, ROUND(AVG(e.G3), 2) as Avg_Final_Grade
FROM BEHAVIOR_LOG b
JOIN ENROLLMENT e ON b.Student_ID = e.Student_ID
GROUP BY b.Weekend_Alcohol
ORDER BY b.Weekend_Alcohol;